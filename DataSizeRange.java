import java.lang.*;

class DataSizeRange
{
	public static void main(String[] args)
	{	
		//For Integer
		System.out.println("Integer Max value: " + Integer.MAX_VALUE);
		System.out.println("Integer Min value: " + Integer.MIN_VALUE);
		System.out.println("Integer Byte value: " + Integer.BYTES);

		//For Float
		System.out.println("Float Max value: " + Float.MAX_VALUE);
		System.out.println("Float Min value: " + Float.MIN_VALUE);
		System.out.println("Float Byte value: " + Float.BYTES);

		//For Character
		System.out.println("Char Max value: " + Character.MAX_VALUE);
		System.out.println("Char Min value: " + Character.MIN_VALUE);
		System.out.println("Char Byte value: " + Character.BYTES);
	}
}
		